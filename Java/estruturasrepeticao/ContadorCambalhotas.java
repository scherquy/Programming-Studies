package estruturasrepeticao;

public class ContadorCambalhotas {
    public static void main(String[] args) {
        
        int contador = (int) 0;

        while(contador < 4){
            System.out.printf("\n%dº cambalhota", contador+1);
            contador++;
        }

        System.out.printf("\n\n");
    }
}
